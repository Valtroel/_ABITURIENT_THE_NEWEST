SET TERM ^ ;

CREATE OR ALTER
    TRIGGER
        DEL_ABITURIENT
FOR
    ABITURA
ACTIVE
    BEFORE DELETE
        POSITION 0
AS
BEGIN
    DELETE FROM
        EXAMEN
    WHERE
        EXAMEN.NOMER_AB     = OLD.NOMER_AB;


    DELETE FROM
        SERTIFICAT
    WHERE
        SERTIFICAT.NOMER_AB = OLD.NOMER_AB;


    DELETE FROM
        AB_P
    WHERE
        AB_P.NOMER_AB       = OLD.NOMER_AB;
END
^

SET TERM ; ^
